/**
 * Get recommended feeds based on bird age and type
 * @param {string} birdType - 'broiler' or 'layer'
 * @param {number} ageInDays - Age of birds in days
 * @returns {Array} Array of recommended feeds
 */
export function getRecommendedFeeds(birdType, ageInDays) {
  if (birdType === 'layer') {
    if (ageInDays < 126) { // Before 18 weeks
      return FEED_BRANDS.starter.concat(FEED_BRANDS.grower);
    } else {
      return FEED_BRANDS.layer;
    }
  } else { // broiler
    if (ageInDays <= 28) { // 0-4 weeks
      return FEED_BRANDS.starter;
    } else if (ageInDays <= 42) { // 4-6 weeks
      return FEED_BRANDS.grower;
    } else { // 6+ weeks
      return FEED_BRANDS.finisher;
    }
  }
}

/**
 * Get local feed mix recommendation
 * @param {string} birdType - 'broiler' or 'layer'
 * @param {number} ageInDays - Age of birds in days
 * @returns {Object} Local feed mix data
 */
export function getLocalFeedMix(birdType, ageInDays) {
  if (birdType === 'layer') {
    if (ageInDays < 126) {
      return ageInDays <= 28 ? LOCAL_FEED_MIXES.starter : LOCAL_FEED_MIXES.grower;
    } else {
      return LOCAL_FEED_MIXES.layer;
    }
  } else {
    if (ageInDays <= 28) {
      return LOCAL_FEED_MIXES.starter;
    } else if (ageInDays <= 42) {
      return LOCAL_FEED_MIXES.grower;
    } else {
      return LOCAL_FEED_MIXES.finisher;
    }
  }
}

/**
 * Calculate cost of local feed mix per kg
 * @param {Object} feedMix - Feed mix object
 * @returns {number} Cost per kg in NGN
 */
export function calculateLocalFeedCost(feedMix) {
  return feedMix.ingredients.reduce((total, ingredient) => {
    return total + (ingredient.percentage / 100) * ingredient.pricePerKg;
  }, 0);
}